    <h1>Handmade Sticky Notes</h1>
    <p>what you need</p>
    <ul>
        <li>Printables</li>
        <li>Scissors</li>
        <li>Double sided tape</li>
        <li>Clear glue</li>
        <li>thin cardboard</li>
    </ul>
    <p>Instructions:</p>
    <ol>
        <li>Cut the notes into the right shape</li>
        <li>Do the same for the cardboard cut the same shape</li>
        <li>Add glue on very thin layer on the part where you want the tape to be </li>
        <li>place the tape on top of the glue</li>
        <li>Set aside to dry</li>
        <li>After 15 minutes start putting the sticky notes on top of each other to create the notes</li>
    </ol>
